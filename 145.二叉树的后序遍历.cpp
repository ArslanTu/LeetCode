class Solution {
public:
    vector<int> res;
    vector<int> postorderTraversal(TreeNode* root) {
        afs(root);
        return res;
    }
    void afs(TreeNode* root){
        if(root==nullptr) return;
        afs(root->left);
        afs(root->right);
        res.insert(res.end(),root->val);
    }
};
