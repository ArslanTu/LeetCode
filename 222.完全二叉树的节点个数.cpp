class Solution {
private:
    int ans=0;
    void dfs(TreeNode* root) {
        if (!root) return;
        ++ans;
        dfs(root->left);
        dfs(root->right);
    }
public:
    int countNodes(TreeNode* root) {
        ans=0;
        dfs(root);
        return ans;
    }
};
